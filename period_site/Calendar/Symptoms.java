public class Symptoms {
    public final boolean cramps;
    public final boolean headache;
    public final boolean fatigue;
    public final boolean moodSwings;

    public Symptoms(boolean cramps, boolean headache, boolean fatigue, boolean moodSwings) {
        this.cramps = cramps;
        this.headache = headache;
        this.fatigue = fatigue;
        this.moodSwings = moodSwings;
    }
}